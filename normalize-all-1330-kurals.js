data.kurals.forEach((k) => {
  if (fixes[k.number]) {
    k.line1 = fixes[k.number].l1;
    k.line2 = fixes[k.number].l2;
  } else {
    const allWords = `${k.line1} ${k.line2}`.trim().split(/\s+/);
    if (allWords.length === 7) {
      k.line1 = allWords.slice(0, 4).join(' ');
      k.line2 = allWords.slice(4, 7).join(' ');
    }
  }
});

let invalid1 = 0;
let invalid2 = 0;

data.kurals.forEach((k) => {
  const l1Words = k.line1.trim().split(/\s+/);
  const l2Words = k.line2.trim().split(/\s+/);
  if (l1Words.length !== 4) invalid1++;
  if (l2Words.length !== 3) invalid2++;
});

